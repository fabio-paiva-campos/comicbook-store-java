    public void atualizaTabela() {
        DefaultTableModel model = (DefaultTableModel) tabelaConsulta.getModel();
        ArrayList<hqs> arrConsulta = usuarioDAO.listar();
        int numRows = arrConsulta.size();
        model.setNumRows(numRows);

        for (int i = 0; i < numRows; i++) {
            model.setValueAt(arrConsulta.get(i).getId(), i, 0);
            model.setValueAt(arrConsulta.get(i).getTitulo(), i, 1);
            model.setValueAt(arrConsulta.get(i).getEditora(), i, 2);
            model.setValueAt(arrConsulta.get(i).getAutor(), i, 3);
            model.setValueAt(arrConsulta.get(i).getPaginas(), i, 4);
            model.setValueAt(arrConsulta.get(i).getVolume(), i, 5);
            model.setValueAt(arrConsulta.get(i).getGenero(), i, 6);
            model.setValueAt(arrConsulta.get(i).getCapa(), i, 7);
        }
    }

    public void limpaCampos() {
        txtTitulo.setText("");
        txtEditora.setText("");
        txtAutor.setText("");
        txtPaginas.setText("");
        txtVolume.setText("");
        txtGenero.setText("");
        lblcapa1.setIcon(null);
    }
    
    public void carregacapa() {
        String filename = (String) tabelaConsulta.getValueAt(linha, 7);
        ImageIcon icon = new ImageIcon(filename); 
        lblcapa1.setIcon(icon);
    }

