    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int input = JOptionPane.showConfirmDialog(null, "Você deseja excluir estas informações?");
        if (input == 0) {
            hqs.setId((int) tabelaConsulta.getValueAt(linha, 0));
            usuarioDAO.remover(hqs);
            atualizaTabela();
            limpaCampos();
            btnExcluir.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Dado excluído com Sucesso!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

